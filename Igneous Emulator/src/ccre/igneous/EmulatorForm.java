    public FloatInputPoll getAnalog(int id) {
        final FloatInputPoll base = getAnalogValue(id);
        return new FloatInputPoll() {
            @Override
            public float readValue() { // Returns in the -3.5v to 3.5v range.
                return 7 * base.readValue() / 4095f - 3.5f;
            }
        };
    }

