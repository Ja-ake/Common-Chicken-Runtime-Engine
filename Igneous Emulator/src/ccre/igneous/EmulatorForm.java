    private FloatOutput getMotorColored(int id, final Color color, final float minInput, final float maxInput) {
        final JProgressBar jpb = motors[id - 1];
        EventQueue.invokeLater(new Runnable() {
            @Override
            public void run() {
                jpb.setForeground(color);
            }
        });
        return new FloatOutput() {
            @Override
            public void writeValue(final float f) {
                EventQueue.invokeLater(new Runnable() {
                    @Override
                    public void run() {
                        jpb.setValue(Math.round(((f - minInput) / (maxInput - minInput)) * (jpb.getMaximum() - jpb.getMinimum()) + jpb.getMinimum()));
                    }
                });
            }
        };
    }

